package com.es.core.model.user;

public class Level {
    private int number;
    private long minXP;
    private long maxXP;

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public long getMinXP() {
        return minXP;
    }

    public void setMinXP(long minXP) {
        this.minXP = minXP;
    }

    public long getMaxXP() {
        return maxXP;
    }

    public void setMaxXP(long maxXP) {
        this.maxXP = maxXP;
    }
}
